public class Fence {
	public final static int SIZE = 20;
	private int x;
	private int y;
	
	public Fence(int newX, int newY){
		x = newX;
		y = newY;
	}
	
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	
}


